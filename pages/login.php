                'username' => array('required' => true),
                'password' => array('required' => true)
        ));

        if($validation->passed()) {
            $user = new User();
            $login = $user->login(Input::get('username'),Input::get('password'));

            if($login) {
                Redirect::to('home');
            }
            else {
                echo 'Login failed';
            }
        }
        else {
            foreach ($validation->errors() as $error) {
                echo $error . '<br>';
            }
        }
    }

